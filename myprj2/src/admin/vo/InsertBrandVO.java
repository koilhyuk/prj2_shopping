package admin.vo;

public class InsertBrandVO {
	private String b_name, b_img;

	public InsertBrandVO(String b_name, String b_img) {
		super();
		this.b_name = b_name;
		this.b_img = b_img;
	}

	public String getB_name() {
		return b_name;
	}

	public String getB_img() {
		return b_img;
	}
	



}
