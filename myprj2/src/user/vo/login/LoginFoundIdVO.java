package user.vo.login;

public class LoginFoundIdVO {
	private String name, phone, id;

	public LoginFoundIdVO(String name, String phone, String id) {
		super();
		this.name = name;
		this.phone = phone;
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public String getPhone() {
		return phone;
	}

	public String getId() {
		return id;
	}

	

	
}//class
